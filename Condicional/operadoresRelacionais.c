#include <stdio.h>

int main(){
    int n1 = 10, n2 =20;

    printf("N1: %i \nN2: %i\n\n", n1, n2);

    printf("N1 > N2? %i \n", n1>n2);
    printf("N1 < N2? %i \n", n1<n2);
    printf("N1 >= N2? %i \n", n1>=n2);
    printf("N1<=N2? %i\n", n1<=n2);
    printf("N1 == N2? %i\n", n1==n2);
    printf("N1 != N2? %i\n", n1!=n2);
    return 0;
}